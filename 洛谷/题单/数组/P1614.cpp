#include <vector>
#include <climits>
#include <iostream>
using namespace std;

int main() {
    int n, m;
    cin >> n >> m;

    vector<int> arr(n, 0);
    for (int i = 0; i < n; i++) {
        cin >> arr[i];
    }

    int result = INT_MAX;

    for (int i = 0; i < n - m + 1; i++) {
        int temp = 0;

        for (int j = 0; j < m; j++) {
            temp += arr[i + j];
        }

        result = min(result, temp);
    }

    cout << result << endl;

    return 0;
}
